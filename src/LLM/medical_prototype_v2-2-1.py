def get_hira_hospitals(sido_nm, sigungu_nm, asm_item_nm, num=5):
    url = "http://apis.data.go.kr/B551182/hospAsmInfoService1/getHospAsmInfo1"
    params = {
        'serviceKey': HIRA_SERVICE_KEY,
        'pageNo': '1', 'numOfRows': str(num * 3),
        'sidoNm': sido_nm, 'sigunguNm': sigungu_nm, 'asmItemNm': asm_item_nm,
    }
    try:
        resp = requests.get(url, params=params, timeout=8)
        if resp.status_code != 200: return []
        items = ET.fromstring(resp.content).findall('.//item')
        result = []
        for item in items:
            g = lambda tag: (item.find(tag).text or '') if item.find(tag) is not None else ''
            addr = g('addr')
            grade = g('asmGrd')
            if sido_nm and sido_nm not in addr: continue       # 시도명 필터 추가
            if sigungu_nm and sigungu_nm not in addr: continue
            # 의원급 제외 (병원급 이상만)
            cl_cd = g('clCdNm')
            if '의원' in cl_cd and '병원' not in cl_cd:
                continue
            result.append({
                'name': g('yadmNm'), 'addr': addr, 'tel': g('telno'),
                'grade': f"{grade}등급" if grade.isdigit() else grade,
                'type': g('clCdNm'),
            })
        return result[:num]
    except Exception:
        return []

